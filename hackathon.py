def normalize_skills(raw_skills):

    skills = raw_skills.split(",")

    normalized = []

    for skill in skills:

        skill = skill.strip().lower()

        if skill in SKILL_ALIASES:

            normalized.append(
                SKILL_ALIASES[skill]
            )

    return list(set(normalized))

