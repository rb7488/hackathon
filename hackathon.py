def cosine_similarity(a, b):

    dot_product = 0

    for i in range(len(a)):

        dot_product += a[i] * b[i]

    norm_a = math.sqrt(
        sum(x*x for x in a)
    )

    norm_b = math.sqrt(
        sum(x*x for x in b)
    )

    if norm_a == 0 or norm_b == 0:

        return 0

    return dot_product / (norm_a * norm_b)

